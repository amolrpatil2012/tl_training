/*
 * 		Java is not fully Object Oriented Language
 * 
 * 		Primitive Types	:	byte/short/int/long /char/boolean/float/double
 * 
 * 		Reason -- Primitive Types are not Objects
 * 
 * 		java.lang package
 * 		|
 * 		Byte				byte
 * 		Short				short
 * 		Long				long
 * 		Integer				int
 * 		Float				float
 * 		Double				double
 * 		Character			char
 * 		Boolean				boolean
 * 
 * 		Wrapper classes -- wraps simple data values into objects
 * 
 */
public class WrapperClassDemo {

	public static void main(String[] args) {
		
			int x = 100;			// Simple Type -- Stack
			
			Integer x1 = 100;		// Object Type -- Heap
			
			int y = 200;
			Integer t = y;			/// Boxing -- conversion from simple to object
			
			
			
			
		
	}
}
